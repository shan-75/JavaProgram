package Javacoding;

public class SwitchVowel {
    public static void main(String[] args) {
        char letter= 'd';

        switch (letter){
            case 'a':
                System.out.println("vowel");
            case 'e':
                System.out.println("vowel");
            case 'i':
                System.out.println("vowel");
            case 'o':
                System.out.println("vowel");
            case 'u':
                System.out.println("vowel");
            default:
                System.out.println("consonant");
        }

    }
}